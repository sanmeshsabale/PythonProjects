# Creating a set 
set1 = set(["My", "Name", "is","Albert"]) 
print("\nSet with the use of List: ") 
print(set1)
# Accessing element using 
# for loop 
print("\nElements of set: ") 
for i in set1: 
    print(i, end=" ") 
  
# Checking the element 
# using in keyword 
print("is" in set1)
